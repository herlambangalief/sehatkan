var jml_plain=document.getElementById("jumlah").value;
var tanggal_plain=document.getElementById("tanggal").value;

var jml_split=jml_plain.split(",");
var tanggal_split=tanggal_plain.split(",");

var tanggal=[];
var jumlah=[];

for (var i = 0; i < jml_split.length; i++) {
  jumlah.push(jml_split[i]);  
}

for (var i = 0; i < tanggal_split.length; i++) {
  tanggal.push(tanggal_split[i]);  
}

